<?php
namespace Goods\Bean;
/**
 * @date: 2018/09/29
 */
class GoodsAttrClassBean extends \Common\Bean\AbstractBean {

    protected $id; // 自增ID';
    protected $name;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name): void
    {
        $this->name = $name;
    }

}